public class Student {
    private String name = "John";
    private String lop = "CO2";
    public Student(){
        name = "John";
        lop = "C02";
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public String getLop() {
        return lop;
    }

    public void setLop(String lop) {
        this.lop = lop;
    }
}


